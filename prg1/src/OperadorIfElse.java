public class OperadorIfElse {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 3;

        if (num2 > num1) {
            System.out.println( num2 + " es mayor que " + num1);
        }else {
            System.out.println( num1 + " es mayor que " + num2);
        }

        num1 = 5;
        num2 = 10;

        if (num2 > num1) {
            System.out.println( num2 + " es mayor que " + num1);
        }else {
            System.out.println( num1 + " es mayor que " + num2);
        }


    }
}

